type OrderedValues<Keys extends readonly string[], Value> = { [Index in keyof Keys]: Value };

function orderedRecord<Keys extends readonly string[], Value>(keys: Keys, values: OrderedValues<Keys, Value>) {
  return Object.fromEntries(keys.map((key, index) => [key, values[index]])) as Record<Keys[number], Value>;
}

