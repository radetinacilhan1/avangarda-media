const COPY: Record<Lang, AlgorithmLocaleCopy> = { sr, en, tr, fr, de, es, el, ar };

function assertRecordKeys(label: string, value: Record<string, unknown>, expected: readonly string[]) {
  const actual = Object.keys(value);
  if (actual.length !== expected.length || expected.some((key) => !(key in value))) {
    throw new Error(`Algorithm copy ${label} does not match the engine structure.`);
  }
}

function assertNoEmptyText(value: unknown, path: string) {
  if (typeof value === "string" && !value.trim()) throw new Error(`Empty algorithm copy at ${path}.`);
  if (Array.isArray(value)) value.forEach((item, index) => assertNoEmptyText(item, `${path}.${index}`));
  if (value && typeof value === "object") {
    Object.entries(value as Record<string, unknown>).forEach(([key, item]) => assertNoEmptyText(item, `${path}.${key}`));
  }
}

Object.entries(COPY).forEach(([lang, copy]) => {
  assertRecordKeys(`${lang}.topics`, copy.topics, ALGORITHM_TOPIC_IDS);
  assertRecordKeys(`${lang}.decisions`, copy.decisions, ALGORITHM_DECISION_IDS);
  assertRecordKeys(`${lang}.choices`, copy.choices, ALGORITHM_CHOICE_IDS);
  assertRecordKeys(`${lang}.events`, copy.events, ALGORITHM_EVENT_IDS);
  assertRecordKeys(`${lang}.endings`, copy.endings, ALGORITHM_ENDING_IDS);
  assertRecordKeys(`${lang}.formats`, copy.formats, ALGORITHM_FORMAT_IDS);
  assertNoEmptyText(copy, lang);
});

export function getAlgorithmCopy(lang: Lang) {
  return COPY[lang];
}

export function getAlgorithmSearchKeywords(lang: Lang) {
  const copy = COPY[lang];
  return [copy.gameTitle, copy.gameSubtitle, copy.typeLabel, copy.seoDescription, ...copy.searchKeywords];
}
